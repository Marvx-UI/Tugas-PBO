public class kondisiswitch1 {
    public static void main(String[] args) {
        int b = 2;

        switch (b) {
            case 1:
                System.out.println("Nilai b adalah 1");
                break;

            case 2:
                System.out.println("Nilai b adalah 2");
                break;

            case 3:
                System.out.println("Nilai b adalah 3");
                break;

            default:
                System.out.println("Tidak ada nilai yang benar");
        }
    }
}
